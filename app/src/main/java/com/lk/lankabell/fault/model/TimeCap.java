package com.lk.lankabell.fault.model;

public class TimeCap {

    private String RawName;
    private String Date;

    public TimeCap() {
    }

    public String getRawName() {
        return RawName;
    }

    public void setRawName(String rawName) {
        RawName = rawName;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }
}
